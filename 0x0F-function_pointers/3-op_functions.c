#include "3-calc.h"
/**
 * op_add - sums 2 numbers
 * @a: integer
 * @b: integer
 * Return: sum of a and b
 */
int op_add(int a, int b)
{
return (a + b);
}

/**
 * op_sub - subtracts 2 numbers
 * @a: integer
 * @b: integer
 * Return: the diffrence of a and b
 */
int op_sub(int a, int b)
{
return (a - b);
}

/**
 * op_mul - multiply 2 numbers
 * @a: integer
 * @b: integer
 * Return: product of a and b
 */
int op_mul(int a, int b)
{
return (a * b);
}

/**
  * op_div - divides 2 numbers
  * @a: integer
  * @b: integer
  * Return: result of dividing of a by b
  */
int op_div(int a, int b)
{
return (a / b);
}

/**
 * op_mod - remainder of 2 numbers division
 * @a: integer
 * @b: integer
 * Return: reminder of dividing a by b
 */
int op_mod(int a, int b)
{
return (a % b);
}
