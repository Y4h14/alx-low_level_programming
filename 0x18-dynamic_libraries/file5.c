/**
 * add - adds two numbers
 * @a: first number
 * @b:second number
 * Return: a + b
 */
int add(int a, int b)
{
    return (a + b);
}

/**
 * add - subtract two numbers
 * @a: first number
 * @b:second number
 * Return: a - b
 */
int sub(int a, int b)
{
    return (a - b);
}

/**
 * mult - multiply two numbers
 * @a: first number
 * @b:second number
 * Return: a * b
 */
int mul(int a, int b)
{
    return (a * b);
}

/**
 * div - divides two numbers
 * @a: first number
 * @b:second number
 * Return: a / b
 */
int div(int a, int b)
{
    return (a / b);
}

/**
 * mod - calculate the mod of two numbers
 * @a: first number
 * @b:second number
 * Return: a / b
 */
int mod(int a, int b)
{
    return (a % b);
}